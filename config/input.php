<?php

/*
|--------------------------------------------------------------------------
| CLEAN STRING INPUT
|--------------------------------------------------------------------------
*/
function clean_input($data) {
    return htmlspecialchars(trim($data), ENT_QUOTES, 'UTF-8');
}

/*
|--------------------------------------------------------------------------
| SAFE LIKE SEARCH
|--------------------------------------------------------------------------
*/
function like_input($data) {
    return '%' . clean_input($data) . '%';
}